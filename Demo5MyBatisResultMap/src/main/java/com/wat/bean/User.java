package com.wat.bean;

public class User {
        private   Integer   id;
        private   String    name;
        private   String    mima;
		public Integer getId() {
			return id;
		}
		public void setId(Integer id) {
			this.id = id;
		}
		public String getName() {
			return name;
		}
		public void setName(String name) {
			this.name = name;
		}
		public String getMima() {
			return mima;
		}
		public void setMima(String mima) {
			this.mima = mima;
		}
}
